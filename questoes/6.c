#include <stdio.h>
#include <stdlib.h>

int main(){
    int *pvet;
    int n;
    scanf("%d", &n);
    pvet = (int *)malloc(n*sizeof(int));
    for(int i=0; i<n; i++){
        scanf("%d", &pvet[i]);
    }
    for(int i=0; i<n; i++){
        printf("%d\n", pvet[i]);
    }
    free(pvet);
    return 0;
}
